package Arrays;

public class Array {
    public static void main(String[] args) {
        String[] breakfast = new String[3];
        breakfast[0] = "apple";
        breakfast[1] = "Milk";
        breakfast[2] = "eggs";

        for(int i = 0; i < breakfast.length; i++){
            System.out.println(breakfast[i]);
        }


    }

}
